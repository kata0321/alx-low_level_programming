#include <stdio.h>
/**
 * add - add two integer
 * @a: first int
 * @b: second int
 * return: addition
 */
int add(int a, int b)
{
return (a + b);
}
/**
 * div - divided two integer
 * @a: first int
 * @b: second int
 * return: finale answer
 */
int div(int a, int b)
{
return (a / b);
}
/**
 * mod - mod of two integer
 * @a: first int
 * @b: second int
 * return: final answer
 */
int mod(int a, int b)
{
return (a % b);
}
/**
 * mul - multplication of two integer
 * @a: first int
 * @b: second int
 * return: final answer
 */
int mul(int a, int b)
{
return (a * b);
}
/**
 * sub - subtract two integer
 * @a: first int
 * @b: second int
 * return: subtracted
 */
int sub(int a, int b)
{
return (a - b);
}
